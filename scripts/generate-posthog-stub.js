import { mkdirSync, writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"

export function writePosthogKey(value, source) {
	mkdirSync("src/generated", { recursive: true })
	writeFileSync(
		"src/generated/posthog-key.generated.ts",
		`// Auto-generated by ${source} — do not edit.\n` +
			`export const KIMCHI_POSTHOG_API_KEY = ${JSON.stringify(value)}\n`,
	)
}

if (fileURLToPath(import.meta.url) === process.argv[1]) {
	writePosthogKey("", "scripts/generate-posthog-stub.js")
}
